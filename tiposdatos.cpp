#include <iostream>

using namespace std;

int main()
{
    cout << "El tamaño de un int es de:\t" << sizeof(int) << " bytes \n";
    cout << "El tamaño de un short int es de:\t" << sizeof(short) << " bytes \n";
    cout << "El tamaño de un long int es de:\t" << sizeof(long) << " bytes \n";
    cout << "El tamaño de un char  es de:\t" << sizeof(char) << " bytes \n";
    cout << "El tamaño de un float es de:\t" << sizeof(float) << " bytes \n";
    cout << "El tamaño de un double es de:\t" << sizeof(double) << " bytes \n";
    cout << "El tamaño de un bool es de:\t" << sizeof(bool) << " bytes \n";
    return 0;
}
